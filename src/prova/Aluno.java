package prova;

public class Aluno {

	private String nome;
	private String matricula;
	
	public Aluno() {
		
	}
	public Aluno(String nome, String mat) {
		this.nome = nome;
		this.matricula = mat;
	}
	
	public String getNome() {
		return nome;
	}
	public String getMatricula() {
		return matricula;
	}	
	
	

}
